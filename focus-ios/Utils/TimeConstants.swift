import Foundation

let OneMonthInMilliseconds = 30 * OneDayInMilliseconds
let OneWeekInMilliseconds = 7 * OneDayInMilliseconds
let OneDayInMilliseconds = 24 * OneHourInMilliseconds
let OneHourInMilliseconds: Int64 = 60 * 60 * 1000
